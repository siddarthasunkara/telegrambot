# ─────────────────────────────────────────────────────
# TRANSLATE HELPER
# ─────────────────────────────────────────────────────

def t(language, key, **kwargs):
    """
    Get a translated string for the given language and key.
    Falls back to English if key missing in that language.
    Usage: t("telugu", "welcome", shop_name="Priya Cakes")
    """
    lang_strings = STRINGS.get(language, STRINGS["english"])
    template = lang_strings.get(key) or STRINGS["english"].get(key, key)
    try:
        return template.format(**kwargs)
    except KeyError:
        return template


