# LANGUAGE PICKER KEYBOARD
# ─────────────────────────────────────────────────────

LANGUAGE_OPTIONS = [
    ("🇬🇧 English",   "english"),
    ("🇮🇳 हिंदी",      "hindi"),
    ("తెలుగు",        "telugu"),
    ("ಕನ್ನಡ",         "kannada"),
    ("தமிழ்",         "tamil"),
    ("മലയാളം",       "malayalam"),
]

def language_picker_keyboard():
    """
    Returns an InlineKeyboardMarkup with all 6 language buttons.
    Each button sends callback_data like: lang_telugu
    """
    from telebot.types import InlineKeyboardMarkup, InlineKeyboardButton
    m = InlineKeyboardMarkup(row_width=2)
    buttons = [
        InlineKeyboardButton(label, callback_data=f"lang_{code}")
        for label, code in LANGUAGE_OPTIONS
    ]
    m.add(*buttons)
    return m